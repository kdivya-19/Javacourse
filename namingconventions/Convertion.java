package namingconventions;

public class Convertion {
	
	public static void main(String[] args) {
		int num=89;
		float f1=(float)num;
		System.out.println("Integer to float: "+f1);
		
		double d1=(double)num;
		System.out.println("Integer to double: "+d1);
		
		byte b=(byte)d1;
		System.out.println(" double to byte: "+b);
		
	}

}
